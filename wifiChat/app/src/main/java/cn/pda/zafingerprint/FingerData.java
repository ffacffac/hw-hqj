package cn.pda.zafingerprint;

public class FingerData {

	private byte[] finger ;
	private String name ;
	
	
	public byte[] getFinger() {
		return finger;
	}
	public void setFinger(byte[] finger) {
		this.finger = finger;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
